public class Koordinat {
    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    private int y;
    private int x;


    public Koordinat(int y, int x) {
        this.y = y;
        this.x = x;
    }
}
